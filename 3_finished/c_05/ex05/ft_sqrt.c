int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 1)
		return (0);
	if (nb > 0)
	{
		while (i < nb / i)
		{
			i++;
		}
	}
	if (i * i != nb)
		return (0);
	return (i);
}
